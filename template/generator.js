function buildI18nFromConfig(config) {
    // If config includes raw i18nData, use it directly
    // Otherwise, this is a minimal fallback
    return config.i18nData || { en: {} };
}

function generateI18nJS(i18nData, config) {
    const langs = (config.i18n && config.i18n.languages) || ['en'];
    const defaultLang = (config.i18n && config.i18n.default) || 'en';

    const i18nJSON = JSON.stringify(i18nData, null, 4);

    return `// i18n translations (generated by template engine)
const i18n = ${i18nJSON};

// Get browser language or default
function getBrowserLanguage() {
    const lang = navigator.language || navigator.userLanguage;
${langs.map(l => {
    const prefix = l === 'jp' ? 'ja' : l;
    return `    if (lang.startsWith('${prefix}')) return '${l}';`;
}).join('\n')}
    return '${defaultLang}';
}

// Set language globally
function setLanguage(lang) {
    document.documentElement.lang = lang;

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (i18n[lang] && i18n[lang][key]) {
            el.innerHTML = i18n[lang][key];
        }
    });

    // Update placeholder translations
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (i18n[lang] && i18n[lang][key]) {
            el.placeholder = i18n[lang][key];
        }
    });

    // Update language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Update diary entry content visibility
    document.querySelectorAll('.diary-entry').forEach(entry => {
        entry.querySelectorAll('.entry-content').forEach(content => {
            if (content.classList.contains(lang)) {
                content.classList.add('active');
            } else {
                content.classList.remove('active');
            }
        });
    });

    // Update toggle button text
    document.querySelectorAll('.entry-toggle').forEach(btn => {
        const entry = btn.closest('.diary-entry');
        if (entry && entry.classList.contains('collapsed')) {
            btn.textContent = i18n[lang] ? i18n[lang].read_more : 'Read more ↓';
        } else {
            btn.textContent = i18n[lang] ? i18n[lang].collapse : 'Collapse ↑';
        }
    });

    localStorage.setItem('preferred-lang', lang);
}

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferred-lang');
    const initialLang = savedLang || getBrowserLanguage();
    setLanguage(initialLang);

    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            setLanguage(btn.dataset.lang);
        });
    });
});
`;
}

