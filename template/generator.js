// Template Generator: config → static site files
// Takes a site config JSON and produces { 'index.html': ..., 'css/style.css': ..., 'js/main.js': ..., ... }

const fs = require('fs');
const path = require('path');
const { generateThemeCSS } = require('./themes/theme.js');

// Block templates
const heroBlock = require('./blocks/hero.js');
const aboutBlock = require('./blocks/about.js');
const shopBlock = require('./blocks/shop.js');
const diaryBlock = require('./blocks/diary.js');
const projectsBlock = require('./blocks/projects.js');
const guestboardBlock = require('./blocks/guestboard.js');
const footerBlock = require('./blocks/footer.js');

const BLOCK_MAP = {
    hero: heroBlock,
    about: aboutBlock,
    shop: shopBlock,
    diary: diaryBlock,
    projects: projectsBlock,
    guestboard: guestboardBlock,
    footer: footerBlock,
};

function generate(config, options = {}) {
    const blocks = config.blocks || ['hero', 'about', 'footer'];
    const langs = (config.i18n && config.i18n.languages) || ['en'];
    const defaultLang = (config.i18n && config.i18n.default) || 'en';
    const agent = config.agent || {};
    const i18nData = config.i18nData || buildI18nFromConfig(config);

    // 1. Generate theme CSS
    const themeCSS = generateThemeCSS(config.theme);

    // 2. Load base CSS (everything after :root block in the source CSS)
    const baseCSS = loadBaseCSS(options.baseCssPath);

    // 3. Combine: theme :root + base CSS body
    const fullCSS = themeCSS + '\n\n' + baseCSS;

    // 4. Generate block HTML
    const blockHtmlParts = blocks
        .filter(name => BLOCK_MAP[name])
        .map(name => BLOCK_MAP[name](config, i18nData));

    // 5. Build payment modal HTML (only if shop block is included)
    const paymentModal = blocks.includes('shop') ? generatePaymentModal(config, i18nData) : '';

    // 6. Generate language switcher
    const langSwitcher = generateLangSwitcher(config);

    // 7. Generate bubbles background
    const bubblesHtml = `    <!-- Floating Bubbles Background -->
    <div class="bubbles">
        <div class="bubble"></div>
        <div class="bubble"></div>
        <div class="bubble"></div>
        <div class="bubble"></div>
        <div class="bubble"></div>
    </div>`;

    // 8. Assemble full HTML
    const title = agent.pageTitle || `${agent.name || 'Agent'} ${agent.mascotEmoji || ''}`.trim();
    const googleFonts = config.theme && config.theme.googleFontsUrl
        ? `    <link rel="preconnect" href="https://fonts.googleapis.com">\n    <link href="${config.theme.googleFontsUrl}" rel="stylesheet">`
        : `    <link rel="preconnect" href="https://fonts.googleapis.com">
    <link href="https://fonts.googleapis.com/css2?family=Nunito:wght@400;600;700;800&family=M+PLUS+Rounded+1c:wght@400;700&display=swap" rel="stylesheet">`;

    // Determine which JS files are needed
    const jsScripts = ['js/i18n.js', 'js/main.js'];
    if (blocks.includes('guestboard')) jsScripts.push('js/guestboard.js');

    const scriptTags = jsScripts.map(s => `    <script src="${s}"></script>`).join('\n');

    const html = `<!DOCTYPE html>
<html lang="${defaultLang}">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>${escapeHtml(title)}</title>
    <link rel="stylesheet" href="css/style.css">
${googleFonts}
</head>
<body>
${bubblesHtml}

${langSwitcher}

${blockHtmlParts.join('\n\n')}

${paymentModal}

${scriptTags}
</body>
</html>`;

    // 9. Generate i18n.js
    const i18nJS = generateI18nJS(i18nData, config);

    // 10. Generate main.js
    const mainJS = generateMainJS(config, blocks);

    // 11. Generate guestboard.js (if needed)
    const files = {
        'index.html': html,
        'css/style.css': fullCSS,
        'js/i18n.js': i18nJS,
        'js/main.js': mainJS,
    };

    if (blocks.includes('guestboard')) {
        files['js/guestboard.js'] = generateGuestboardJS(config);
    }

    return files;
}

function loadBaseCSS(customPath) {
    // Try custom path first, then default locations
    const candidates = [
        customPath,
        path.join(__dirname, '..', 'css', 'style.css'),  // Current site's CSS
        path.join(__dirname, 'themes', 'base.css'),       // Fallback base
    ].filter(Boolean);

    for (const p of candidates) {
        try {
            const css = fs.readFileSync(p, 'utf8');
            // Strip the :root { ... } block (we generate our own)
            return css.replace(/:root\s*\{[^}]*\}/s, '/* :root variables generated by theme engine */');
        } catch (e) {
            continue;
        }
    }

    // Minimal fallback
    return '/* No base CSS found - using minimal defaults */\n* { margin: 0; padding: 0; box-sizing: border-box; }';
}

function generateLangSwitcher(config) {
    const langs = (config.i18n && config.i18n.languages) || ['en'];
    if (langs.length <= 1) return '';

    const langLabels = { zh: '中', en: 'En', jp: 'あ', ko: '한', es: 'Es', fr: 'Fr', de: 'De' };
    const defaultLang = (config.i18n && config.i18n.default) || langs[0];

    const buttons = langs.map(lang => {
        const label = langLabels[lang] || lang.slice(0, 2);
        const active = lang === defaultLang ? ' active' : '';
        return `        <button class="lang-btn${active}" data-lang="${lang}">${label}</button>`;
    }).join('\n');

    return `    <!-- Language Switcher -->
    <div class="lang-switch">
${buttons}
    </div>`;
}

