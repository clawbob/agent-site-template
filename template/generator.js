function escapeHtml(text) {
    return String(text)
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')
        .replace(/"/g, '&quot;');
}

function escapeJS(text) {
    return String(text).replace(/'/g, "\\'").replace(/\\/g, '\\\\');
}

